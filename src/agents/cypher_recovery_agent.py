# agents/cypher_recovery_agent.py

import re
import logging
from typing import Optional
from agents.llm_router import init_llm

logger = logging.getLogger(__name__)

def repair_cypher_text(cypher: str) -> str:
    """
    Apply heuristic repairs to Cypher queries generated by LLMs.
    
    Repairs handled:
    - Appending missing RETURN clause if query ends with MATCH/WITH/WHERE
    - Fixing swapped list comprehension [expr | var IN list] -> [var IN list | expr]
    - Stripping comments for cleaner check
    """
    # 1. Strip comments for accurate terminal check
    cypher_clean = re.sub(r'//.*', '', cypher).strip()
    
    # 2. Fix Incomplete Terminal Clauses (ends with MATCH, WITH, or WHERE)
    words = cypher_clean.split()
    if words:
        last_word = words[-1].upper()
        if last_word in ["MATCH", "WITH", "WHERE"] or "RETURN" not in cypher.upper():
            cypher += "\nRETURN * LIMIT 50"
            logger.info(f"Auto-appended missing RETURN clause (triggered by {last_word}).")

    # 3. Fix Swapped List Comprehension: [u.id | u IN list] -> [u IN list | u.id]
    # Neo4j 5 syntax is [var IN list | expression]
    pipe_swap_match = re.search(r"\[([\w\.]+)\s*\|\s*(\w+)\s+IN\s+([^\]]+)\]", cypher, re.IGNORECASE)
    if pipe_swap_match:
        expr, var, lst = pipe_swap_match.groups()
        repaired = f"[{var} IN {lst} | {expr}]"
        cypher = cypher.replace(pipe_swap_match.group(0), repaired)
        logger.info(f"Auto-repaired Cypher comprehension: {repaired}")

    return cypher.strip()

def validate_cypher_safety(cypher: str) -> Optional[str]:
    """
    Check for illegal or banned Cypher constructs.
    Returns error message if unsafe, else None.
    """
    # 1. Banned GROUP BY (Not a thing in Cypher, usually a hallucination)
    if "GROUP BY" in cypher.upper():
        return "CYPHER SYNTAX ERROR: Illegal 'GROUP BY' clause detected. Use implicit grouping (e.g. RETURN label, count(*))."
    
    # 2. Banned Parameters ($ is often misused to halluncinate vars)
    if "$" in cypher:
        return "CYPHER SYNTAX ERROR: Illegal parameter (starts with $) detected. Do NOT use parameters ($threshold, etc.). Use literal values directly in the query."
    
    # 3. Banned size() on relationships (Neo4j 5 deprecation)
    if "size(" in cypher.lower() and "-[" in cypher.lower():
        return "CYPHER SYNTAX ERROR: 'size((n)-[]-())' is deprecated in Neo4j 5. Use 'COUNT { (n)-[]-() }' instead."

    return None

def generate_repaired_cypher(
    question: str,
    last_cypher: Optional[str],
    last_error: Optional[str],
    schema_context: str,
    llm_id: str
) -> str:
    """
    Request LLM to generate or repair a Cypher query.
    """
    llm = init_llm(llm_id)
    
    error_feedback = ""
    if last_error:
        error_feedback = f"\n\nCRITICAL FIX REQUIRED: The previous attempt failed with error: {last_error}\nCommon fix: Ensure all variables in RETURN are defined in MATCH or WITH."

    prompt = (
        "You are an expert Neo4j developer. Write a Cypher query to answer the user's question.\n"
        f"Schema: {schema_context}\n"
        f"Question: {question}{error_feedback}\n\n"
        "Rules:\n"
        "1. Output ONLY the Cypher query (use ```cypher ... ```). No explanations.\n"
        "2. ALWAYS LIMIT results to 50 nodes (LIMIT 50).\n"
        "3. VARIABLE SCOPE: If you use 'WITH', you MUST pass through EVERY variable you intend to use in RETURN.\n"
        "4. MANDATORY TERMINAL RETURN: Every query MUST end with a RETURN clause.\n"
        "5. LIST COMPREHENSION: Syntax is [variable IN list | expression].\n"
        "6. ILLEGAL / BANNED CONSTRUCTS:\n"
        "   - NEVER use 'GROUP BY'.\n"
        "   - NEVER use 'size((n)-[]-(m))'. Use 'COUNT { (n)-[]-(m) }'.\n"
        "   - NEVER use 'relationships()'.\n"
        "7. VISUALIZATION: Always return nodes ('n', 'u', etc.) or paths ('p').\n"
        "8. NO PARAMETERS: Do NOT use parameters like '$threshold' or '$param'. Use literal numbers or strings (e.g. 5, 'High').\n"
        "9. UNION CONSISTENCY: If you use UNION, all subqueries MUST return the exact same column names in the exact same order.\n"
        "10. NO SUBQUERIES IN LISTS: Never put `(MATCH ...)` inside a list comprehension `[var IN (...) | ...]`. Instead, use `CALL { MATCH ... RETURN var }` or just simple WITH/MATCH combinations.\n"
        "11. UNION ENDING: If using UNION, the query ends with the final subquery's RETURN. DO NOT add an extra global RETURN at the end.\n"
    )
    
    resp = llm.invoke(prompt)
    content = resp.content.strip()
    
    # Extract from markdown
    match = re.search(r"```(?:cypher)?(.*?)```", content, re.DOTALL | re.IGNORECASE)
    cypher = match.group(1).strip() if match else content.replace("`", "").strip()
    
    # Apply heuristic repairs
    cypher = repair_cypher_text(cypher)
    
    return cypher
